#include "libft.h"
#include <ctype.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*string;

	i = 0;
	string = (char *)s;
	while (n > i)
	{
		string[i] = 0;
		i++;
	}
}
