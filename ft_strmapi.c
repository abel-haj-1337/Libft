#include "libft.h"
#include <stdlib.h>

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned long	i;
	char			*string;

	if (!s)
		return (NULL);
	i = 0;
	string = (char *)malloc(ft_strlen(s) + 1);
	if (string == NULL)
		return (NULL);
	while (s[i])
	{
		string[i] = f(i, s[i]);
		i++;
	}
	string[i] = '\0';
	return (string);
}
